# Erste Zeichenkette
x = "15.3"

ergebnis = x * 2  # Zeichenkette 2 mal ausgeben
print(ergebnis)

x = float(x)
ergebnis = x * 2  # Kommazahl mal 2
print(ergebnis)

# Zweite Zeichenkette
x = "17"

ergebnis = x * 2  # Ganze Zahl mal 2
print(ergebnis)

x = int(x)
ergebnis = x * 2
print(ergebnis)
